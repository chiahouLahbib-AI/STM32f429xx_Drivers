/******************************************************************************************/
/**		*@brief 	Timer Config																															*/
/**		*@param 	*Tim : Timer Base Address																									*/
/**		*@param		*Tim_Config :	Pointer to the configuration struct send by appli						*/
/**		*@retval	None																																			*/
/******************************************************************************************/

void Tim_Cfg(TIM_TypeDef *Tim, Tim_Cfg_t *Tim_Cfg)
{
	
	/* Update request source */
	Tim->CCR1 |= (uint32_t)(1UL << 2);
	
	Tim->CCR1 |= (uint32_t)(1UL << 4);
	
	Tim->CNT = Tim_Cfg->Counter_u8;
	
	Tim->PSC = Tim_Cfg->Prescaler_u8;
	
	Tim->ARR = Tim_Cfg->Auto_Reload_Reg_u8;
	
	/* Enable Iterrupt */
	Tim->DIER |= (1UL << 0);

	/* Update generation */
	Tim->EGR |= (1UL << 0);
	

}




